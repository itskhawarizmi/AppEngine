namespace AppEngine
{
    /// <summary>
    /// Defines any file type extensions.
    /// </summary>
    public enum FileTypeExtension
    {
        /// <summary>
        /// The file extension for Plain Text File.
        /// </summary>
        TXT = 1,

        /// <summary>
        /// The file extension for Microsoft Word Document.
        /// </summary>
        DOC = 2,

        /// <summary>
        /// The file extension for Portable Document Format.
        /// </summary>
        PDF = 3,

        /// <summary>
        /// The file extension for Log File.
        /// </summary>
        LOG = 4,

    }
}
